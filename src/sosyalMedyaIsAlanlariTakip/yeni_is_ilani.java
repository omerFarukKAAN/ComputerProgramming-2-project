    private void cmb_sektörActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmb_sektörActionPerformed
        // TODO add your handling code here:
        switch (cmb_sektör.getSelectedIndex()) {
            case 0:
                cmb_meslek.removeAllItems();
                break;
            case 1:
                cmb_meslek.removeAllItems();
                cmb_meslek.addItem("Bilgisayar Mühendisi");
                cmb_meslek.addItem("Mobil Yazılım Uzmanı");
                cmb_meslek.addItem("Teknik Destek Elemanı");
                cmb_meslek.addItem("Yazılım Mühendisi");
                break;
            case 2:
                cmb_meslek.removeAllItems();
                cmb_meslek.addItem("Elektronik Ticaret");
                break;
            case 3:
                cmb_meslek.removeAllItems();
                cmb_meslek.addItem("Aşçı");
                cmb_meslek.addItem("Aşçı");
                cmb_meslek.addItem("Diyetisten");
                cmb_meslek.addItem("Gurme");
                cmb_meslek.addItem("Gıda Mühendisi");
                cmb_meslek.addItem("Süt ve Süt Ürünleri Teknikeri");
                break;
            case 4:
                cmb_meslek.removeAllItems();
                cmb_meslek.addItem("Mimar");
                cmb_meslek.addItem("İnşaat Mühendisi");
                cmb_meslek.addItem("Kule Vinç Operatörü");
                cmb_meslek.addItem("İş Sağlığı ve Güvenliği Elemanı");
                cmb_meslek.addItem("İş Makineleri Bakımcısı");
                break;
            case 5:
                cmb_meslek.removeAllItems();
                cmb_meslek.addItem("Ev Tekstil Konfeksiyon Teknisyeni");
                cmb_meslek.addItem("Kalıp Teknisyeni");
                cmb_meslek.addItem("Tekstil Mühendsii");
                cmb_meslek.addItem("Tekstil Terbiye Teknisyeni");
                break;
        }
    }//GEN-LAST:event_cmb_sektörActionPerformed


//                    Ilan.bilisimIlanlari.add(i);
//                    break;
//                case 2:
//                    Ilan.eTicaretIlanlari.add(i);
//                    break;
//                case 3:
//                    Ilan.gidaIlanlari.add(i);
//                    break;
//                case 4:
//                    Ilan.insaatIlanlari.add(i);
//                    break;
//                case 5:
